#input two numbers from user
number_1 = int(input("Enter the first number:"))
number_2 = int(input("Enter the second number:"))
#add two numbers
print('{} + {} = {}'.format(number_1, number_2, number_1 + number_2))
#subtract two numbers
print('{} - {} = {}'.format(number_1, number_2, number_1 - number_2))
#multiply two numbers
print('{} * {} = {}'.format(number_1, number_2, number_1 * number_2))
#divide two numbers
print('{} / {} = {}'.format(number_1, number_2, number_1 / number_2))
